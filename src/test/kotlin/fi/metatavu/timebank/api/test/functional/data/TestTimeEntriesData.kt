        fun generateRandomForecastTimeEntries(pageNumber: Int): List<ForecastTimeEntry> {
            val generatedEntries = mutableListOf<ForecastTimeEntry>()
            var id = (100000..900000).random()
            val amountToGenerate = if (pageNumber == 1) 1000 else if (pageNumber == 2) 998 else 0
            while (generatedEntries.size <= amountToGenerate) {
                val year = (2021..2022).random()
                val month = (1..12).random()
                val day =
                    if (month == 2) (1..28).random()
                    else if (month % 2 != 0 && month < 8) (1..31).random()
                    else if (month % 2 == 0 && month >= 8
                    ) (1..31).random()
                    else (1..30).random()
                val monthString = if (month < 10) "0$month" else "$month"
                val dayString = if (day < 10) "0$day" else "$day"
                val date = "$year-$monthString-$dayString"
                val person = (100000..900000).random()
                generatedEntries.add(
                    ForecastTimeEntry(
                        id = id,
                        person = person,
                        project = null,
                        card = null,
                        task = null,
                        non_project_time = if ((0..1).random() != 0) 1 else null,
                        time_registered = (1..435).random(),
                        date = date,
                        notes = null,
                        approval_status = null,
                        created_by = person,
                        updated_by = person,
                        created_at = "${date}T12:00:00+02:00",
                        updated_at = "${date}T12:00:00+02:00",
                        phase = null,
                        task_project = null,
                        invoice_entry = null,
                        invoice = null
                    )
                )
                id++
            }
            return generatedEntries
        }
